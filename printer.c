#include <unistd.h>

void printer(int array[9][9])
{
	int i = 0;
	int x = 0;
	int y = 0;
	char chara;
	while (y < 9)
	{
		chara = array[y][x] + 48;
		write(1,&chara,1);
		x++;
		if(x >= 9)
		{
			x = 0;
			y++;
			write(1,"\n",1);
		}
	}
}
